function orange(){
	setStorage('orange');
	var nav=document.getElementById("nav");
	var background1=document.getElementById("nav_li_first1");
	var bordercolor1=document.getElementById("article_right_div1");
	var borderTopcolor1=document.getElementById("article_left_middle_div4_nav_li");
	var color=document.getElementsByClassName("article_right_div1_2_li_a_color");
	for(var i=0;i<color.length;i++){
		color[i].style.color="#ffa400";
	}
	var color1=document.getElementsByClassName("article_right_div3_dl_a");
	for(var i=0;i<color1.length;i++){
		color1[i].style.color="#ffa400";
	}
	for(var i=1;i<12;i++){
		var color_2_n=document.getElementById("color_2_"+i);
		color_2_n.style.color="#ffa400";
	}
	for(var i=1;i<5;i++){
		var color_3_n=document.getElementById("color_3_"+i);
		color_3_n.style.color="#ffa400";
	}
	borderTopcolor1.style.borderTopColor="#ffa400";
	borderTopcolor1.style.color="#ffa400";
	nav.style.borderTopColor="#ffa400";
	background1.style.backgroundColor="#ffa400";
	bordercolor1.style.borderColor="#ffa400";
	var span_color=document.getElementById("color_span");
	span_color.style.backgroundColor="#ffa400";

}//改变为橙色
