function setStorage(e){
	localStorage.setItem('theme',e);
}
function getStorage(){
	return localStorage.getItem('theme')?localStorage.getItem('theme'):'red';
}
window.onload=function(){
	if(getStorage()){
		switch(getStorage()){
			case 'red':
				red();
				break;
			case 'blue':
				blue();
				break;
			case 'orange':
				orange();
				break;
			case 'green':
				green();
				break;
			case 'yellow':
				yellow();
				break;
		}
	}
}